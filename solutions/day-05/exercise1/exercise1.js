// What is props in a React component ?
// - is a property of a component, can pass data from parent component down to child components

// How do you access props in a React component ?
// - we pass the word 'props' into a React component and then extract the property from props
// - ex: props.firstName

// What data types can we pass as props to components ?
// - strings, numbers, arrays, objects, functions


// What is a propTypes?
// - a package that helps with assigning data types to props passed to components

// What is a default propTypes?
// - setting premade props or default props incase a user does not enter one themselves

